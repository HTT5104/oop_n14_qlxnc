    private void save_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_ButtonActionPerformed
        // TODO add your handling code here:
            
            birth_Certificate.setId(id_Field.getText());
            birth_Certificate.setName(name_Field.getText());
            birth_Certificate.setDob((String) dob_Day_Combobox.getSelectedItem() + dob_Month_Combobox.getSelectedItem() + dob_Year_Combobox.getSelectedItem());
            birth_Certificate.setIsMale(isMale_Rbutton.isSelected());
            birth_Certificate.setNation((String) nation_Combobox.getSelectedItem());
            birth_Certificate.setBirth_Place(birth_Place_Field.getText());
            birth_Certificate.setEthnic((String) ethnic_Combobox.getSelectedItem());
            birth_Certificate.setFather(father_Field.getText());
            birth_Certificate.setMother(mother_Field.getText());
            birth_Certificate.setRegistration_Place(regist_Place_Field.getText());
            birth_Certificate.setRegistration_Date((String)regist_Year_Combobox.getSelectedItem() + regist_Day_Combobox.getSelectedItem() + regist_Month_Combobox.getSelectedItem());
            birth_Certificate.setRegistrant(registrant_Field.getText());
            if (listener != null) {
                    
                    listener.onDataReceived(birth_Certificate); // Gọi phương thức callback
                }
                dispose();
 
    }//GEN-LAST:event_save_ButtonActionPerformed

